            <h2>教材不是萬能，沒教材萬萬不能  -  唐宗浩，自由數學共同發起人</h2>
            <h3>為什麼要編教材？</h3>
            <img src="/static/img/Bestian001.jpg" width="50%">
            <p>其實，一開始我和許多教育工作者一樣，只是在教自己班上的學生，有需要時，會自編一些學習單而已，沒想過會去編整套的教材。</p>
            <p>那時我總認為編整套的教材，是有人力、有財力的出版社，或是公益的基金會，才做的事。</p>
            <p>後來是因為教材不夠用，我為了要找一套，可以自學用的教材，去賣參考書的書店逛。</p>
            <p><b>結果，我被看到的一切嚇到了。找了很久，找不到一本有完整交代觀念的。它們都把重點放在後面的練習題。</b></p>
            <p>這意味著，學生不能用這些教材自學，甚至無法邊學邊問，甚至在觀念還不清楚的情況下，就被埋入大量的習題之中。</p>
            <p><b>這不是我要的。我要學生可以拿來自己學會的。</b></p>
            <p>事實上，很多學生在課堂上不會完全聽懂，聽懂也可能會忘，教材應該具有學習和複習的功能，尤其是先備知識有洞的學生，更需要適合程度的教材來補洞。</p>
            <p>例如對一個「分數」概念不清的學生，你給他二十題分數的「計算」練習，並不能幫他澄清概念。很多人以為學生數學不好就是練習不夠，其實不一定，很多時候是觀念不清。練習也重要，但不能本末倒置。</p>
            <p>沒有完整引入觀念的教材，只能說是公式與題目集錦，只能當額外練習，完完全全不適合學生自學。</p>
            <p><b>那時我十分沮喪。後來升起一個念頭：既然，我要的教材不存在，那就只好自己去編了。</b></p>
            <h3>和佳仁老師合作</h3>
            <p>佳仁是我在教育界的前輩，也是我在小學時的理化老師，後來我在自主培力學園任教時，她也給過很多教學方面的指導。</p>
            <p>佳仁在自主培力學園請產假的那一學年，我大學還沒畢業，就接下了代課的任務。</p>
            <p>代佳仁的課，一開始壓力很大，因為佳仁的課很精彩，也很紮實，而且自主培力學園是挑戰很高的班級。</p>
