            <p>正好那時佳仁回來，我們就商量自編「形成性教材」，也就是可以自修來形成概念，可以當課本用的教材。</p>
            <p>其實，如果不是佳仁決定一起合作，我自己一個人可能不敢著手去編。</p>
            <h3>編教材的過程</h3>
            <p>編一份教材的過程，並沒有想像中簡單。並不是把自己會的，寫出來就好。</p>
            <p>形成性教材，是把課堂上的引入、講述、探問、深化、綜合、練習等過程，用紙本呈現。</p>
            <p>課堂上，同一個概念可以用三四種不同的角度切入，可以跟學生持續對話，直到他懂為止。教材就沒有辦法，同一個概念大約只能用一兩個例子和代表性的圖解，來做引入，所以設計的過程就必須格外小心。如果弄偏了，學生自己唸的時候就會看不懂。</p>
            <p><b>還有要考量的就是文字的量。對於閱讀障礙的學生，不管內容這是什麼，字太多、排版太緊，就會沒辦法讀。</b>既然教材要讓閱讀障礙的學生也能使用（當然難免還是會有些吃力），就必須在脈絡清楚的前提下，將字數精簡、將版面簡化、也不宜放太多花俏的裝飾。</p>
            <p>對我來說，<b>編一份搭配上課的輔助教材，大約要一個小時，但是編一份可以自修用的形成性教材，大約要一個星期。</b></p>
            <p>兩者的難度，是天壤之別。</p>
            <h3>學習地圖</h3>
            <p>學習地圖原本的初始想法，其實只是一份打勾用的清單，把各單元依年段列上去。</p>
            <p>後來決定嘗試用先備後續的關係，畫成有向圖、拉出學習地圖，上面也標註各單元被排入的年級，這樣學生不一定要依年段來唸，可以依主題領域去自修。有些學生喜歡深入掌握一個主題，例如幾何，先把國中各個幾何的單位弄通，再去碰代數，而不是依照課本進度，一個年級各碰一些。</p>
            <p><b>有這樣一張地圖，無論是想照年級去自修，或是想照主題領域去自修的學生，都可以各得其所。</b></p>
            <p>等到編小學學習地圖時，發現這個結構不適用，因為小學很多都是交互相關、適合用浸泡式、整合在一起學習。</p>
            <p>而且小學的領域也不大適合完全自修，所以地圖的編法是以親子數學為基本，給家長看的。</p>
            <p>至於高中地圖，目前還沒有編得很完善。不過初步可以看出，高中的單元幾乎都是為了大學的數學做準備，有些是截取其中一小角來教，並不真的很必要。</p>
            <p>像微積分、線性代數、機率、統計、離散數學都是這樣，被截取一角放入高中課程。因此在地圖上就標出它們原本來自哪裡，如果有興趣的學生，可以自己去找原初的知識領域，完整學起來，瞭解更高的整體，回頭來看被選入高中課程的片段，就會覺得有脈絡可循而比較簡單了。</p>
            <h3>開源的想法</h3>
            <p><b>事實上，一開始我對開源也很陌生，是不是作品白白讓人家拿去用？</b></p>
            <p>但是，後來我覺得，不必為了防止少數人可能濫用，就去阻止多數人善用它的機會。而且，這套教材如果一直留在我們自己的檔案庫中，很可能一直都是半成品。</p>
            <p><b>那些沒編完的單元，尤其是高中部份，需要畫大量的圖解，其實不是只靠兩名各自有家庭、有工作的人，用課餘的時間，在接下來的三五年內，能夠完成的。</b></p>
            <p>如果開源出去，說不定會影響一些人加入協作吧？說不定會有其他的親子數學實踐家長，和適性教學的老師，參與一起開發吧？光是為了這個可能，就值得了。</p>
